#import <Foundation/Foundation.h>
int main(int argc, const char * argv[])
{
    @autoreleasepool {
        // 实例化NSObject对象赋值给id类型
        id obj = [[NSObject alloc]init];
        // 实例化数组赋值给id类型
        id array = [[NSArray alloc]init];
        // 字符串赋值给id类型
        id str = @"Hello World";
        // NSObject init 方法的定义
        // - (id)init;
    }
    return 0;
}

